    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
         if(Double.parseDouble(lblcredit.getText())==Double.parseDouble(lbldebit.getText())){
           cntrl.updatePayment(txtvchno.getText(),datef.format(paymentDate.getDate()),tblAccount,modeltbl,accountname1,amount1,accountname2,amount2);
           this.dispose();
         }else{
             JOptionPane.showMessageDialog(this,"Debit and Credit Amounts should be Equal");
             if(Double.parseDouble(lblcredit.getText())>Double.parseDouble(lbldebit.getText())){
                amounttype.setSelectedIndex(0);
                amounttype.requestFocusInWindow();
                txtamount.setText(Format.ToString(Double.parseDouble(lblcredit.getText())-Double.parseDouble(lbldebit.getText())));
             }
             if(Double.parseDouble(lbldebit.getText())>Double.parseDouble(lblcredit.getText())){
                amounttype.setSelectedIndex(1);
                amounttype.requestFocusInWindow();
                txtamount.setText(Format.ToString(Double.parseDouble(lbldebit.getText())-Double.parseDouble(lblcredit.getText())));
            }
         }
    }//GEN-LAST:event_btnsaveActionPerformed

    private void amounttypeItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_amounttypeItemStateChanged
        if(amounttype.getSelectedIndex()==0){
            nameList.removeAllItems();
            cntrl.fetchaccountsford(nameList);
            if(Double.parseDouble(lblcredit.getText())>Double.parseDouble(lbldebit.getText())){
                txtamount.setText(Format.ToString(Double.parseDouble(lblcredit.getText())-Double.parseDouble(lbldebit.getText())));
            }
        }
        else if(amounttype.getSelectedIndex()==1){
            nameList.removeAllItems();
            cntrl.fetchaccountsforc(nameList);
            if(Double.parseDouble(lbldebit.getText())>Double.parseDouble(lblcredit.getText())){
                txtamount.setText(Format.ToString(Double.parseDouble(lbldebit.getText())-Double.parseDouble(lblcredit.getText())));
            }
        }
    }//GEN-LAST:event_amounttypeItemStateChanged

    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        if(!nameList.getSelectedItem().toString().equals("") && !txtamount.getText().equals("")){
            Object[] data=new Object[6];
            dltbrow(modeltbl.getRowCount());
            data[0]=tblAccount.getRowCount()+1;
            data[1]=amounttype.getSelectedItem();
            data[2]=nameList.getSelectedItem();
            if(amounttype.getSelectedIndex()==0){
                data[3]=txtamount.getText();
                data[4]="";
                lbldebit.setText(Format.ToString(Double.parseDouble(lbldebit.getText())+Double.parseDouble(txtamount.getText())));
            }
            else if(amounttype.getSelectedIndex()==1){
                data[3]="";
                data[4]=txtamount.getText();
                lblcredit.setText(Format.ToString(Double.parseDouble(lblcredit.getText())+Double.parseDouble(txtamount.getText())));
            }
            data[5]=txtdetail.getText();
            modeltbl.addRow(data);
            addbrow(modeltbl.getRowCount()-1);
            nameList.setSelectedIndex(0);
            txtamount.setText("");
            txtdetail.setText("");
            if( !lblcredit.getText().equals("00.00") && Double.parseDouble(lbldebit.getText())>Double.parseDouble(lblcredit.getText()) ){
                amounttype.setSelectedIndex(1);
                txtamount.setText(Format.ToString(Double.parseDouble(lbldebit.getText())-Double.parseDouble(lblcredit.getText())));
            }
            if( !lbldebit.getText().equals("00.00") && Double.parseDouble(lblcredit.getText())>Double.parseDouble(lbldebit.getText())){
                amounttype.setSelectedIndex(0);
                txtamount.setText(Format.ToString(Double.parseDouble(lblcredit.getText())-Double.parseDouble(lbldebit.getText())));
            }
        }else{
            JOptionPane.showMessageDialog(this,"Fill All Details..","Insert..!",2);
        }
        
    }//GEN-LAST:event_btnaddActionPerformed

