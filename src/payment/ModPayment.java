    private void btndeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeleteActionPerformed
        int row=tblAccount.getSelectedRow();
        if(row<0){
           JOptionPane.showMessageDialog(this,"Select Row...","Select...",1);
        } 
        else if(row>=tblAccount.getRowCount()-16){
            JOptionPane.showMessageDialog(this,"This Row will not Delete","Delete row",1);
        }
        else{
            int n=JOptionPane.showConfirmDialog(this,"Do you want to delete this?", "Delete?", JOptionPane.YES_NO_OPTION,3);
            if(n==JOptionPane.YES_OPTION){
                if(tblAccount.getValueAt(row, 1).toString().equals("D"))
                    lbldebit.setText(Format.ToString(Double.parseDouble(lbldebit.getText())-Double.parseDouble((String) tblAccount.getValueAt(row, 3))));
                if(tblAccount.getValueAt(row, 1).toString().equals("C"))
                    lblcredit.setText(Format.ToString(Double.parseDouble(lblcredit.getText())-Double.parseDouble((String) tblAccount.getValueAt(row, 4))));
                modeltbl.removeRow(row);
                for(int i=row;i<tblAccount.getRowCount()-16;i++)
                    tblAccount.setValueAt(i+1, i,0);
            }
        }
    }//GEN-LAST:event_btndeleteActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        int row=tblAccount.getSelectedRow();
        if(row<0){
           JOptionPane.showMessageDialog(this,"Select Row...","Select...",1);
        } 
        else if(row>=tblAccount.getRowCount()-16){
            JOptionPane.showMessageDialog(this,"This Row will not Update","Update row",1);
        }
        else{
            int n=JOptionPane.showConfirmDialog(this,"Do you want to Update this?", "Update?", JOptionPane.YES_NO_OPTION,3);
            if(n==JOptionPane.YES_OPTION){
                amounttype.setSelectedItem(tblAccount.getValueAt(row, 1));
                nameList.setSelectedItem(tblAccount.getValueAt(row, 2));
                if(tblAccount.getValueAt(row, 1).toString().equals("D")){
                    lbldebit.setText(Format.ToString(Double.parseDouble(lbldebit.getText())-Double.parseDouble((String) tblAccount.getValueAt(row, 3))));
                    txtamount.setText((String) tblAccount.getValueAt(row, 3));
                }
                if(tblAccount.getValueAt(row, 1).toString().equals("C")){
                    lblcredit.setText(Format.ToString(Double.parseDouble(lblcredit.getText())-Double.parseDouble((String) tblAccount.getValueAt(row, 4))));
                    txtamount.setText((String) tblAccount.getValueAt(row, 4));
                }
                txtdetail.setText((String) tblAccount.getValueAt(row, 5));
                modeltbl.removeRow(row);
                for(int i=row;i<tblAccount.getRowCount()-16;i++)
                    tblAccount.setValueAt(i+1, i,0);
            }
        }
    }//GEN-LAST:event_btnupdateActionPerformed

